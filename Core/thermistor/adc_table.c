uint16_t getTemperature(uint16_t ntc_adc_val)
{
    uint16_t i;
    uint16_t max_idx = HIGH_TEMP - LOW_TEMP;

    for (i = 0; i < max_idx && ntc_adc_val > ntc_adc_table[i]; i++);

    if (i == 0) return LOW_TEMP;
    if (i == max_idx) return HIGH_TEMP;

    if (ntc_adc_val - ntc_adc_table[i - 1] < ntc_adc_table[i] - ntc_adc_val)
        return (i - 1) + LOW_TEMP;
    else 
        return i + LOW_TEMP;
}